//用来计算学分绩的函数
function AddFunCalc()
{
	chrome.tabs.query
	(
	    {
	        active: true, 
	        currentWindow: true
	    }, 
	    function(tabs) 
	    {  
	        chrome.tabs.sendMessage
	        (
	            tabs[0].id, 
	            {greeting: "hello"},
	            function(response) 
	            {    
	                if (response === undefined)
	                    alert("这个只能在选课系统-已修课程那里用哦～");
	                else
	                {
                        if (response.error === "error")
                        {
                            alert("这个只能在选课系统-已修课程那里用哦～");
                        }
	                }
	                    
	            }
	        ); 
	    }
	);	
}



